        estado = true;
    Thread hilo = new Thread(){
        public void run(){
            for (;;){
                if(estado==true){
                    try{
                        sleep(1);
                        if (milisegs>=1000){
                            milisegs = 0;
                            segs++;
                        }
                        if(segs >=60){
                        milisegs =0;
                        segs = 0;
                        mins++;
                        
                        }
                        if(mins>=60){
                        milisegs =0;
                        segs = 0;
                        mins = 0;
                        horas++;
                        }
                        jLabel1.setText(horas + " : "+mins + " : "+ segs + " : "+ milisegs);
                        milisegs++;
                    } catch (Exception e) {
                        
                    }
                
                
            }else{
                    
                    break;
                    }
        }
        }
    };
